.MODEL SMALL
.STACK 100H
.DATA
	a1 DW ?
	b1 DW ?
	c1 DW ?
	T1 DW ?
	T2 DW ?
	T3 DW ?
	T4 DW ?
	T5 DW ?
	T6 DW ?
	T7 DW ?
	T8 DW ?
.CODE
;------printing procedure----
PRINT_ID PROC

	;SAVE IN STACK
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX

	;CHECK IF NEGATIVE
	OR AX, AX
	JGE PRINT_NUMBER

	;PRINT MINUS SIGN
	PUSH AX
	MOV AH, 2
	MOV DL, '-'
	INT 21H
	POP AX

	NEG AX

	PRINT_NUMBER:
	XOR CX, CX
	MOV BX, 10D

	REPEAT_CALC:

		;AX:DX- QUOTIENT:REMAINDER
		XOR DX, DX
		DIV BX  ;DIVIDE BY 10
		PUSH DX ;PUSH THE REMAINDER IN STACK

		INC CX

		OR AX, AX
		JNZ REPEAT_CALC

	MOV AH, 2

	PRINT_LOOP:
		POP DX
		ADD DL, 30H
		INT 21H
		LOOP PRINT_LOOP

	;NEWLINE
	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H

	POP DX
	POP CX
	POP BX
	POP AX

	RET
PRINT_ID ENDP

func_a PROC

	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH BP
	MOV BP , SP


	MOV AX, 7
	MOV a1, AX
	ADD SP, 0
	POP BP

	POP DX
	POP CX
	POP BX
	POP AX
RET
func_a ENDP

foo PROC

	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH BP
	MOV BP , SP

	SUB SP , 2

	MOV AX, [BP - 2]
	ADD AX, 3
	MOV T1, AX

	MOV AX, T1
	MOV [BP - 2], AX
	ADD SP, 2
	POP BP

	POP DX
	POP CX
	POP BX
	POP AX
RET
foo ENDP

bar PROC

	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH BP
	MOV BP , SP

	SUB SP , 2
	SUB SP , 2

	MOV AX, 4
	MOV BX, [BP - 2]
	MUL BX
	MOV T2, AX

	MOV AX, 2
	MOV BX, [BP - 4]
	MUL BX
	MOV T3, AX

	MOV AX, T2
	ADD AX, T3
	MOV T4, AX

	MOV AX, T4
	MOV c1, AX
	ADD SP, 4
	POP BP

	POP DX
	POP CX
	POP BX
	POP AX
RET
bar ENDP

MAIN PROC

	;INITIALIZE DATA SEGMENT
	MOV AX, @DATA
	MOV DS, AX

	PUSH BP
	MOV BP , SP

	SUB SP , 2
	SUB SP , 2
	SUB SP , 2
	SUB SP , 2

	MOV AX, 5
	MOV [BP - 2], AX

	MOV AX, 6
	MOV [BP - 4], AX
	CALL func_a
;--------print function called---------

	MOV AX, a1
	CALL PRINT_ID

	MOV AX, [BP - 2]
	MOV [BP - 2], AX
	CALL foo

	MOV AX, T1
	MOV [BP - 6], AX
;--------print function called---------

	MOV AX, [BP - 6]
	CALL PRINT_ID

	MOV AX, [BP - 2]
	MOV [BP - 2], AX

	MOV AX, [BP - 4]
	MOV [BP - 4], AX
	CALL bar

	MOV AX, T4
	MOV [BP - 8], AX
;--------print function called---------

	MOV AX, [BP - 8]
	CALL PRINT_ID

	MOV AX, [BP - 2]
	MOV [BP - 2], AX

	MOV AX, [BP - 4]
	MOV [BP - 4], AX
	CALL bar

	MOV AX, 6
	MOV BX, T4
	MUL BX
	MOV T5, AX

	MOV AX, T5
	ADD AX, 2
	MOV T6, AX

	MOV AX, [BP - 2]
	MOV [BP - 2], AX
	CALL foo

	MOV AX, 3
	MOV BX, T1
	MUL BX
	MOV T7, AX

	MOV AX, T6
	SUB AX, T7
	MOV T8, AX

	MOV AX, T8
	MOV [BP - 4], AX
;--------print function called---------

	MOV AX, [BP - 4]
	CALL PRINT_ID
	ADD SP, 8
	POP BP


	MOV AX, 4CH
	INT 21H
MAIN ENDP

END MAIN

